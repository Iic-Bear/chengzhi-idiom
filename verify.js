function getChars(file) {
  const c = fs.readFileSync(file, 'utf8');
  return [...c.matchAll(/chars:\s*\[(.+?)\]/g)]
    .map(m => [...m[1].matchAll(/'([^']+)'/g)].map(x => x[1]).join(''));
}
